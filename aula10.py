# A diferença do "+" e da "," é que a vírgula da um espaço depois dela, já o "+" junta tudo.
# A concatenação é a junção de um dado com o outro, como os exemplos abaixo. 

concatenacao = 'A' + 'B' + 'C' # Usar o mais em strings, fará com que elas se juntem, formando assim: "ABC".

print(concatenacao)

# Diferente da concatenação, o sinal de multiplicação, tem como função a "repetição", ou seja, ele irá repetir os dados quantas vezes lhe foi pedido. Exemplos abaixo.

a_dez_vezes = 'A' * 10
tres_vezes_paulo = 3 * 'Paulo' # Multiplicar uma string dessa forma, fará com que ela se repita 3 vezes, formando: "PauloPauloPaulo".

print(a_dez_vezes)
print(tres_vezes_paulo)