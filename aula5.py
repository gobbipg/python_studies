# Tipo de dado bool (boolean)
# Ao questionar algo em um programa, só existem duas respostas: sim (True) ou não (False)
# Existem vários operadores para "questionar". Dentre eles o ==, que é um operador lógivo que questiona se um valor é igual a outro 

print(10 == 10) # Sim -> True (Verdadeiro)
print(10 == 11) # Não -> False (Falso)
print(type(True))
print(type(False))
print(type(10 == 10)) # Ao utilizar o type para saber o tipo de dado nesse caso, ele somente lhe retornará o tipo e não mostrarar o valor (que seria True)
print(type(10 == 11))