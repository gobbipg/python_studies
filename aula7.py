# Variáveis são usadas para salvar algo na memória do computador
# PEP8: Inicie variáveis utilizando letras minúsculas, pode usar números e underline (_)
# O sinal de = é um operador de atribuição. Ele é usado para atribuir um valor a um nome (variável)
# Uso: nome_variavel = expressão
# É possível acrecentar praticamente qualquer tipo de valor a uma varável, como está abaixo, e como pode ser usado no print, por exemplo:

nome_completo = 'Paulo Gobbi'
idade = 21
peso = 60.5
maior_de_idade = idade >= 18

print('O', nome_completo, 'tem', idade, 'anos e pesa', peso, 'kilos.') # Posso mesclar strings com as variáveis utilizando as vírgulas, assim formando frases ou coisas parecidas
print(nome_completo, 'é maior de idade?', maior_de_idade)
print('O', nome_completo, 'foi aprovado!')